''' 
Program to print a triangle and square with "*"
'''


def triangleShape():
    # To Print a triangle
    print("\t   *   ")
    print("\t  ***  ")
    print("\t ***** ")
    print("\t*******")


def squareShape():
    # To Print a square
    print("\t*** ***")
    print("\t*******")
    print("\t*** ***")
    print("\t*******")
    print("\t*** ***")
    print("\t*******")
    print("\t*******")

def main():
    triangleShape()
    squareShape()

if __name__ == "__main__":
    main()
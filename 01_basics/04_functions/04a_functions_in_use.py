'''
Program to print a triangle with "*"
'''

def triangle():
    print("\t   *   ")
    print("\t  ***  ")
    print("\t ***** ")
    print("\t*******")


if __name__ == "__main__":
    triangle()


# RESULT:   *   
#          ***  
#         ***** 
#        *******
# Looks like a Christmas tree

